    private void registrarOfertaAcademica() {
        ofertaAcademica oa = new ofertaAcademica();
        ctrlOfertaAcademica ctrloa = new ctrlOfertaAcademica();
    

        if (txt_clave.getText().isEmpty() || txtNombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Llene los campos");
        } else {
            
                oa.setClave_especialidad(txt_clave.getText().trim());
                oa.setNombre_especialidad(txtNombre.getText().trim());

                if (ctrloa.Guadar(oa)) {
                    JOptionPane.showMessageDialog(null, "Registro guardado");
                } else {
                    JOptionPane.showMessageDialog(null, "Error al guardar");
                }

        }

        txtNombre.setText("");
        txt_clave.setText("");

    }

